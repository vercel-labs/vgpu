import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const docsDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const examplesDir = path.join(docsDir, 'examples');
const publicExamplesDir = path.join(docsDir, 'public', 'examples');
const sourcesOutFile = path.join(docsDir, 'lib', 'examples-source.generated.ts');
const thumbsOutFile = path.join(docsDir, 'lib', 'example-thumbs.generated.ts');
const ignoredFolders = new Set(['_shared']);

function langFor(file) {
  if (file.endsWith('.wgsl')) return 'wgsl';
  if (file.endsWith('.tsx')) return 'tsx';
  if (file.endsWith('.ts')) return 'typescript';
  if (file.endsWith('.json')) return 'json';
  return 'text';
}

function parseFilesFromMeta(source) {
  const match = source.match(/files\s*:\s*\[([\s\S]*?)\]/m);
  if (!match) return null;
  const files = [];
  const itemPattern = /['"]([^'"]+)['"]/g;
  let item;
  while ((item = itemPattern.exec(match[1])) !== null) files.push(item[1]);
  return files.length > 0 ? files : null;
}

async function filesForExample(dir) {
  const metaSource = await readFile(path.join(dir, 'meta.ts'), 'utf8');
  const listed = parseFilesFromMeta(metaSource);
  if (listed) return listed;
  const entries = await readdir(dir, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile() && /\.(ts|tsx|wgsl)$/.test(entry.name))
    .map((entry) => entry.name)
    .sort();
}

async function writeSources(slugs) {
  const data = {};
  for (const slug of slugs) {
    const dir = path.join(examplesDir, slug);
    const files = await filesForExample(dir);
    data[slug] = [];
    for (const name of files) {
      const filePath = path.join(dir, name);
      const code = await readFile(filePath, 'utf8');
      data[slug].push({ name, lang: langFor(name), code });
    }
  }

  const source = `// This file is generated by scripts/ingest-examples.mjs. Do not edit by hand.\n\nexport interface ExampleSourceFile {\n  readonly name: string;\n  readonly lang: string;\n  readonly code: string;\n}\n\nexport const exampleSources = ${JSON.stringify(data, null, 2)} as const satisfies Record<string, readonly ExampleSourceFile[]>;\n`;

  await mkdir(path.dirname(sourcesOutFile), { recursive: true });
  await writeFile(sourcesOutFile, source);
  console.log(`Wrote ${path.relative(docsDir, sourcesOutFile)} for ${slugs.length} examples.`);
}

async function writeThumbs() {
  const entries = await readdir(publicExamplesDir, { withFileTypes: true }).catch((error) => {
    if (error && error.code === 'ENOENT') return [];
    throw error;
  });
  const files = new Set(entries.filter((entry) => entry.isFile()).map((entry) => entry.name));
  const slugs = new Set();
  for (const name of files) {
    const match = name.match(/^(.+)\.(card|hero)\.png$/);
    if (match) slugs.add(match[1]);
  }

  const thumbs = {};
  for (const slug of [...slugs].sort()) {
    if (files.has(`${slug}.card.png`) && files.has(`${slug}.hero.png`)) {
      thumbs[slug] = {
        card: `/examples/${slug}.card.png`,
        hero: `/examples/${slug}.hero.png`,
      };
    }
  }

  const source = `// This file is generated by scripts/ingest-examples.mjs. Do not edit by hand.\n\nexport interface ExampleThumbPresence {\n  readonly card: string;\n  readonly hero: string;\n}\n\nexport const exampleThumbs: Record<string, ExampleThumbPresence> = ${JSON.stringify(thumbs, null, 2)};\n`;
  await mkdir(path.dirname(thumbsOutFile), { recursive: true });
  await writeFile(thumbsOutFile, source);
  console.log(`Wrote ${path.relative(docsDir, thumbsOutFile)} for ${Object.keys(thumbs).length} thumbnail sets.`);
}

const entries = await readdir(examplesDir, { withFileTypes: true });
const slugs = entries
  .filter((entry) => entry.isDirectory() && !ignoredFolders.has(entry.name))
  .map((entry) => entry.name)
  .sort();

await writeSources(slugs);
await writeThumbs();
