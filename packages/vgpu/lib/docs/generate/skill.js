// Renders a Claude-style skill (SKILL.md router + references/<doc>, one file per doc) from the docs
// manifest. Same source of truth as the `vgpu docs` CLI — this is just a static, loadable mirror.
// The router stays small on purpose: it lists the guides + an API index and tells the agent to load
// one doc at a time (via references/ or the CLI), instead of reading everything.

/** @returns {Map<string, string>} relative skill path → file content */
export function buildSkill(manifest) {
  const docs = uniqueDocs(manifest.records);
  const files = new Map();
  for (const doc of docs) {
    files.set(`references${doc.virtualPath}`, referenceFile(doc));
  }
  files.set("SKILL.md", router(docs));
  return files;
}

// Many allowlist symbols can map to the same .docs.md; collapse to one reference per file and keep
// the list of symbols it documents.
function uniqueDocs(records) {
  const byPath = new Map();
  for (const record of records) {
    const existing = byPath.get(record.virtualPath);
    if (existing) {
      existing.symbols.push(record.symbol);
    } else {
      byPath.set(record.virtualPath, { ...record, symbols: [record.symbol] });
    }
  }
  return [...byPath.values()];
}

function referenceFile(doc) {
  const provenance = `> Source: \`${doc.virtualPath}\` — generated by \`vgpu generate:docs\`. Edit the source .docs.md, not this file.\n\n`;
  return provenance + doc.content.trimEnd() + "\n";
}

function router(docs) {
  const guides = docs.filter((d) => d.kind === "guide").sort(bySymbol);
  const api = docs.filter((d) => d.kind === "api");
  const packages = [...new Set(api.map((d) => d.package))].sort();
  const symbolCount = api.reduce((n, d) => n + d.symbols.length, 0);

  const out = [
    "---",
    "name: vgpu",
    "description: >-",
    "  Build and optimize WebGPU apps with the vgpu package and entrypoints vgpu/node,",
    "  vgpu/mock, vgpu/scene, and vgpu/client. Use @vgpu/render/inspect, /utils, /edit,",
    "  and /perf only as slim tooling subpaths. Bundles performance guides and the API",
    "  reference; load one doc at a time.",
    "---",
    "",
    "# vgpu",
    "",
    "Generated from the vgpu `.docs.md` source. Each entry maps to a file in `references/` and to a",
    "doc you can load on demand with the CLI — **load only what you need, don't read the whole skill**:",
    "",
    "vgpu is layered: vgpu/core (thin WebGPU wrappers) → vgpu (main API with WGSL reflection) → vgpu/scene (geometry/camera helpers). Lower layers are always accessible and interoperable.",
    "",
    "Glossary: **Surface** means a canvas-backed render target (swapchain) created with `gpu.surface(canvas)`; do not confuse it with the general phrase ‘API surface’.",
    "",
    "```sh",
    "npx --package @vgpu/cli vgpu docs find <query>    # search doc paths + symbols",
    "npx --package @vgpu/cli vgpu docs grep -i <term>  # search doc CONTENT",
    "npx --package @vgpu/cli vgpu docs cat <symbol>    # print one doc, e.g. `cat Frame`, `cat performance-model`",
    "```",
    "",
    "## Performance guides",
    "",
    "Writing or optimizing a shader? Read **performance-model** first, then the rest as needed.",
    "",
  ];
  for (const guide of guides) {
    out.push(`- **${guide.symbol}** — ${summarize(guide.content)}  \`references${guide.virtualPath}\``);
  }
  out.push("");
  out.push("## API reference");
  out.push("");
  out.push(`${symbolCount} symbols across ${packages.length} packages — open \`references/<package>/<file>\` or \`npx --package @vgpu/cli vgpu docs cat <symbol>\`:`);
  out.push("");
  for (const pkg of packages) {
    const symbols = api
      .filter((d) => d.package === pkg)
      .flatMap((d) => d.symbols)
      .sort((a, b) => a.localeCompare(b));
    out.push(`- \`${pkg}\` — ${symbols.join(", ")}`);
  }
  out.push("");
  return out.join("\n");
}

function summarize(content) {
  // Collect the first prose paragraph (sentences may wrap across source lines), skipping headings
  // and blockquotes, then take its first sentence.
  let paragraph = "";
  for (const raw of content.split("\n")) {
    const line = raw.trim();
    if (line.startsWith("#") || line.startsWith(">")) continue;
    if (!line) {
      if (paragraph) break;
      continue;
    }
    paragraph = paragraph ? `${paragraph} ${line}` : line;
  }
  const plain = paragraph
    .replace(/\[([^\]]+)\]\([^)]*\)/gu, "$1") // [text](url) -> text
    .replace(/\*\*/gu, "")
    .replace(/`/gu, "")
    .replace(/\s+/gu, " ");
  const sentence = plain.match(/^(.*?\.)(?:\s|$)/u);
  const text = sentence ? sentence[1] : plain;
  return text.length > 150 ? `${text.slice(0, 147).trimEnd()}…` : text;
}

function bySymbol(a, b) {
  return a.symbol.localeCompare(b.symbol);
}
